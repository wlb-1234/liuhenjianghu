/**
 * 根据行政区划代码获取坐标
 */
export function getCoordinates(code: string): { lat: number; lng: number } | null {
  // 尝试在多个层级查找
  if (cityCoordinates[code]) {
    return cityCoordinates[code];
  }
  
  // 省级坐标（前2位）
  const provinceCode = code.substring(0, 2);
  if (provinceCoordinates[provinceCode]) {
    return {
      lat: provinceCoordinates[provinceCode].lat,
      lng: provinceCoordinates[provinceCode].lng
    };
  }
  
  return null;
}
